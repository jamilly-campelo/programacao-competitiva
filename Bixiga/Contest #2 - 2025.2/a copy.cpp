#include <bits/stdc++.h>
using namespace std;

int main() {
    int t; cin >> t;

    for (int i = 0; i < t; i++) {
        int n; cin >> n;
        
        vector<int> vec(n);
        int k;

        for (int j = 0; j < n; j++) {
            cin >> k;
            vec.pushback(k);
        }
    }

    return 0;
}